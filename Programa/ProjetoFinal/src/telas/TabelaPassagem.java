    private void escreveTabela() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        PassagemCRUD passCRUD = new PassagemCRUD();
        for (Passagem p : passCRUD.buscaTudo()) {
            modelo.addRow(new Object[]{
                p.getId_passagem(),
                p.getNome(),
                p.getCpf(),
                p.getRg(),
                p.getData(),
                p.getHora(),
                p.getPlataforma(),
                p.getOrigem(),
                p.getDestino(),
                p.getValor(),
                p.getTipo()
            });
        }
    }

